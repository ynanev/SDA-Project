package Command;

public class Game {

	public void stop()
	{
		System.out.println("Game stoped");
		
	}

	public void resume()
	{
		System.out.println("Game resumed");
		
	}

	public void pause()
	{
		System.out.println("Game paused");
		
	}

	public void start() 
	{
		System.out.println("Game started");
		
	}

}
