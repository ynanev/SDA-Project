package Command;

public class Game {

	public void stop()
	{   System.out.println("%%%%%%%%%%%%%%%");
		System.out.println("% Game stoped %");
		System.out.println("%%%%%%%%%%%%%%%");
	}

	public void resume()
	{   System.out.println("%%%%%%%%%%%%%%%%");
		System.out.println("% Game resumed %");
		System.out.println("%%%%%%%%%%%%%%%%");
	}

	public void pause()
	{   System.out.println("%%%%%%%%%%%%%%%");
		System.out.println("% Game paused %");
		System.out.println("%%%%%%%%%%%%%%%");
	}

	public void start() 
	{   System.out.println("%%%%%%%%%%%%%%%%");
		System.out.println("% Game started %");
		System.out.println("%%%%%%%%%%%%%%%%");
	}

}
